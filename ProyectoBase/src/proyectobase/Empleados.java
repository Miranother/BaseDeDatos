    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        //Aqui en buscar lo que hace es que pones el id del paciente
        //y automaticamente todos los campos se rellenaran con los datos del usario
        //asi podras ver si los datos y poder modificarlo o eliminar
        Connection con = null;
        try {
            con = getConnection();
            ps = con.prepareStatement("SELECT * FROM Empleados WHERE Id_Empleado = ?");
            ps.setString(1, txtIdEmpleado.getText());
            rs = ps.executeQuery();

            if (rs.next()) {
                txtNombre.setText(rs.getString("Nombre"));
                txtApePat.setText(rs.getString("ApellidoPat"));
                txtApeMat.setText(rs.getString("ApellidoMat"));
                txtTel.setText(rs.getString("Telefono"));
                txtPuesto.setText(rs.getString("Puesto"));
                txtHorario.setText(rs.getString("HorarioLaboral"));
                txtCPostal.setText(rs.getString("CP"));
                txtCalle.setText(rs.getString("CalleNumero"));
                txtCol.setText(rs.getString("Colonia"));
            } else {
                JOptionPane.showMessageDialog(null, "No existe un Empleado con ese Id");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al buscar Empeado: " + e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

